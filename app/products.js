export function getStockStatus(p) {
  if (p.stock === 0) return 'out';
  if (p.stock <= p.minStock) return 'low';
  return 'in';
}

export function getStats(list) {
  return {
    total:        list.length,
    totalUnits:   list.reduce((s, p) => s + p.stock, 0),
    lowStock:     list.filter(p => p.stock > 0 && p.stock <= p.minStock).length,
    outOfStock:   list.filter(p => p.stock === 0).length,
    transactions: 47,
  };
}

export function getCategoryStats(list) {
  return ['Swift', 'Lifting (Purchased)', 'PVE', 'Starlift'].map(cat => ({
    cat,
    count: list.filter(p => p.category === cat).length,
    total: list.filter(p => p.category === cat).reduce((s, p) => s + p.stock, 0),
  }));
}